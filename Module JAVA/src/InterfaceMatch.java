    public void update(){
        try {
            con = DBConnection.connect();
            stmt = con.createStatement();

            String r1 = "select LastName from Categorie where Categorie.type='Player'";
            String r2 = "select LastName from Categorie where Categorie.type='Referee'";
            String r3 = "select LastName from Categorie where Categorie.type='BallBoy'";
            
            rs = stmt.executeQuery(r1);
            Player1.removeAllItems();
            Player1.addItem("Selectionner un joueur");
            Player2.removeAllItems();
            Player2.addItem("Selectionner un joueur");
            while(rs.next()){
                Player1.addItem(rs.getString("LastName"));
                Player2.addItem(rs.getString("LastName"));
            }
            Referee.removeAllItems();
            Referee.addItem("Selectionner un arbitre");
            rs = stmt.executeQuery(r2);
            while(rs.next()){
                Referee.addItem(rs.getString("LastName"));
            }
            BallBoy.removeAllItems();
            BallBoy.addItem("Selectionner un ramasseur de balle");
            rs = stmt.executeQuery(r3);
            while(rs.next()){
                BallBoy.addItem(rs.getString("LastName"));
            }
            
            Date.setText("");

           
        }
        catch(Exception e){
            System.out.println(e);
        }
    }

