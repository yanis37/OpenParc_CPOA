    private void SauvegarderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SauvegarderActionPerformed
        // TODO add your handling code here:
        try{
            String player1 = Player1.getSelectedItem().toString();
            String player2 = Player2.getSelectedItem().toString();
            String referee = Referee.getSelectedItem().toString();
            String ballBoy = BallBoy.getSelectedItem().toString();
            String date = Date.getText();
            
            String rp1 = "Select id from Categorie WHERE Categorie.LastName = '"+player1+"'";
            String rp2 = "Select id from Categorie WHERE Categorie.LastName = '"+player2+"'";
            String rr = "Select id from Categorie WHERE Categorie.LastName = '"+referee+"'";
            String rbb = "Select id from Categorie WHERE Categorie.LastName = '"+ballBoy+"'";
            
            rs = stmt.executeQuery(rp1);
            if(rs.next()){
                IdP1 = rs.getInt("id");
            }
            rs = stmt.executeQuery(rp2);
            if(rs.next()){
                IdP2 = rs.getInt("id");
            }
            rs = stmt.executeQuery(rr);
            if(rs.next()){
                IdR = rs.getInt("id");
            }
            rs = stmt.executeQuery(rbb);
            if(rs.next()){
                IdBB = rs.getInt("id");
            }
            
            String reuquete = "INSERT INTO `Match1` (`id`, `Player1`, `Player2`, `Referee`, `BallBoy`, `Date` ) VALUES (NULL, '"+IdP1+"', '"+IdP2+"','"+IdR+"','"+IdBB+"','"+date+"')";
            stmt.executeUpdate(reuquete);

            
        }catch(Exception e){
            System.out.println(e);
        }
        update();
    }//GEN-LAST:event_SauvegarderActionPerformed

